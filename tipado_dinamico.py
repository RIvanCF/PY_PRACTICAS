'''
Para este programa, la variable valor imprimirá el ultimo dato asignado. 
Es por eso que, para imprimir todos los datos asignados a la variable, se le debe de establecer una impresión a cada uno 
'''
valor = "Work Level"
print("Variable 'valor' tipo string: ",valor)
valor = 940214
print("Variable 'valor' tipo entero: ",valor)
valor = 6.78
print("Variable 'valor' tipo flotante: ",valor)
